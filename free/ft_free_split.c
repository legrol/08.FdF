#include "../includes/fdf.h"

/**
 * The function "ft_free_split" free double pointer string with split.
 * 
 * @param char **split 		String to free.  
 * 
 */

void	ft_free_split(char **split)
{
	int	i;

	i = 0;
	while (split[i])
	{
		free(split[i]);
		i++;
	}
	free(split);
}
